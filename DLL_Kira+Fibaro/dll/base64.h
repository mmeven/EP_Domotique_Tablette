#ifndef BASE64_H_INCLUDED
#define BASE64_H_INCLUDED

#include <string>

/**
 * \file base64.h
 * \brief encoder une chaine de caractères en base64 ou decoder du base64 vers une chaine de caractères
 * \author Corentin Chatellier
 * \version 0.1
 * \date 14/05/2016
 */


/**
* \fn string base64_encode(unsigned char const* bytes_to_encode, unsigned int in_len) : encoder
* \param[in] unsigned char const* bytes_to_encode : la chaine à encoder
* \param[in] unsigned int in_len : taille de la chaine à encoder
* \return string ret : la chaine encodée
 */

std::string base64_encode(unsigned char const* , unsigned int len);

/**
* \fn std::string base64_decode(std::string const& s): décoder
* \param[in] std::string const& s : la chaine à décoder
* \return string ret : la chaine décodée
 */

std::string base64_decode(std::string const& s);

#endif // BASE64_H_INCLUDED
